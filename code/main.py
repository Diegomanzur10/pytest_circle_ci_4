## Logic calculator

def suma(a: float, b:float) -> float:
    return a + b

def resta(a: float, b:float) -> float:
    return a - b

def multiplication(a: float, b:float) -> float:
    return a * b

def division(a: float, b:float) -> float:
    return a / b

def modulo(a: float, b:float) -> float:
    return a % b